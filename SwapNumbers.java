//swap 2 no's
public class SwapNumbers {
    public static void main(String[] args) {
        int num1 = 34;
        int num2 = 4325;
        int temp = num1;
        num1=num2;
        num2 = temp;
        System.out.println("Swapped no's: "+num1);
        System.out.println("Swapped no's:  "+ num2);
    }
}
